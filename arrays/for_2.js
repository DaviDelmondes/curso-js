// indice          0   1   2    3   4   5   6   7
const numeros = [ 68, 55, 123, 22, 95, 33, 45, 77]

// foreach => para cada elemento do arrays
//  numeros de numero
for(numero of numeros){
    console.log(numero)
}


// para cada indice do elemento 
//      i em numeros                                                                                                                
for(let i in numeros){
    console.log(`${i} => ${numeros[i]}`)
}







